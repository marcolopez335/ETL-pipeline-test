SELECT
    AH.ISSUE_KEY AS STORY_NUMBER,
    AH.PROJECT_KEY,
    AH.PROJECT_NAME,
    AH.SPRINT_NAME,
    AH.TEAM_NAME,
    AH.STORY_NAME,
    AH.ISSUE_TYPE AS STORY_TYPE,
    AH.SNAPSHOT_DATE,
    NULL AS LAST_UPDATED,
    AH.CREATED AS CREATE_DATE,
    AH.UPDATED,
    AH.BEGIN_DATE,
    AH.END_DATE,
    AH.SPRINT_ACTIVATE_DATE,
    AH.SPRINT_COMPLETE_DATE,
    AH.TARGET_START,
    AH.TARGET_END,
    AH.INITIAL_DATE_TO_IN_PROGRESS,
    AH.RESOLVED AS CLOSED_DATE,
    AH.STATUS,
    AH.RESOLUTION,
    AH.FIX_VERSION,
    AH.STORY_POINTS AS ESTIMATE,
    AH.PROGRAM_INCREMENT,
    AH.CUSTOMER_EPIC_ID,
    COALESCE(ASUM.CUSTOMER_EPIC, AH.CUSTOMER_EPIC) AS CUSTOMER_EPIC,
    AH.CUSTOMER_CAPABILITY_ID,
    AH.CUSTOMER_CAPABILITY,
    AH.SUB_CAPABILITY_ID,
    AH.SUB_CAPABILITY,
    AH.FEATURE_ID,
    AH.FEATURE,
    AH.EPIC_ID,
    AH.EPIC
FROM AMMM_JIRA_AGILE_HISTORY AH
LEFT JOIN AMMM_JIRA_AGILE_SUMMARY ASUM
ON AH.ISSUE_KEY = ASUM.ISSUE_KEY
